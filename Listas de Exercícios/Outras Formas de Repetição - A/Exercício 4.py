# 4) Quando devemos utilizar o “enquanto”?

# Devemos utilizar o while quando não sabemos antecipadamente quantas vezes o laço será executado. 
# A repetição depende de um evento ou de uma condição que será alterada dinamicamente durante a 
# execução do próprio bloco (exemplo: aguardar o usuário digitar um valor válido, fazer uma busca 
# até encontrar um item específico, ou simulações onde um alvo deve ser alcançado).