# Estratégia para implementar laços complexos

# Quando não é óbvio como construir o laço, a abordagem sistemática recomendada é:

#     Criar exemplos concretos: Invente 2 ou 3 cenários simples de entrada de dados.

#     Resolver manualmente passo-a-passo: Escreva no papel o que você, como humano, faria a cada passo para resolver o problema, prestando atenção em onde você anota os resultados temporários.

#     Identificar o padrão de repetição: Observe as suas anotações manuais e descubra qual sequência de ações exatas se repetiu em todos os passos.

#     Generalizar o padrão em variáveis: Substitua os números concretos do seu exemplo por variáveis que representarão o estado atual em cada repetição do laço.

#     Codificar o laço (As 4 Perguntas): Com base no padrão, escreva a inicialização, a condição de parada, o corpo do padrão e a atualização das variáveis (aplicando a lógica da Pergunta 5).

#     Teste de Mesa: Pegue o código recém-escrito e simule no papel usando outro exemplo concreto para confirmar se o algoritmo alcança o resultado esperado.